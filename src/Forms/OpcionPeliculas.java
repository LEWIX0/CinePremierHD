    public void agregarPelicula(){    //Metodo agregarPelicula.
        try
        {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO peliculas (Id_pelicula,titulo,año,genero,duracion,formato,sala,hora) VALUES (?,?,?,?,?,?,?,?)");
            pst.setString(1, txtIdPelicula.getText());
            pst.setString(2, txtTitulo.getText());
            pst.setString(3, txtAño.getText());
            pst.setString(4, txtGenero.getText());
            pst.setString(5, txtDuracion.getText());
            pst.setString(6, txtFormato.getText());
            pst.setString(7, sala.getSelectedItem().toString());
            pst.setString(8, hora.getSelectedItem().toString());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Se ha guardado correctamente");
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error"+ e.getMessage());
            
        }
    }    
    
