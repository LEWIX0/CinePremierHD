    public String [] gethorario(String sala)   //Metodo para anidar 2 comboBox.
    {
        String [] horarios = new String[5];
        if(sala.equalsIgnoreCase("Sala 1-2D"))
        {
            horarios[0] = "2 PM";
            horarios[1] = "4:30 PM";
            horarios[2] = "7 PM";
            horarios[3] = "8:30 PM";
            horarios[4] = "11 PM";
        }
        
        if(sala.equalsIgnoreCase("Sala 2-3D"))
        {
            horarios[0] = "12 PM";
            horarios[1] = "2:30 PM";
            horarios[2] = "5 PM";
            horarios[3] = "7:30 PM";
            horarios[4] = "10 PM";
        }
        
        if(sala.equalsIgnoreCase("Sala 3-2D"))
        {
            horarios[0] = "4 PM";
            horarios[1] = "6:30 PM";
            horarios[2] = "9 PM";
            horarios[3] = "11:30 PM";
            horarios[4] = "2 AM";
        }
     return horarios;   
    }
